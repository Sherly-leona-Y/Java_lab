public class BitwiseOperators {
    public static void main(String[] args) {
        int a = 5;
        int b = 3;
        System.out.println("Let's perform bitwise operations on a = 5 and b = 3");
        System.out.println("a & b = "+(a&b));
        System.out.println("a / b = "+(a/b));
        System.out.println("a ^ b = "+(a^b));
        System.out.println("a << 2 = "+(a<<2));
        System.out.println("a >> 2 = "+(a >> 2));
    }
}
